/**
 * Interface à implementer par les intepréteurs. Les méthodes interprétant les 7 mots possibles doivent
 * êtres implémentés
 *
 * @author Bruno Malenfant
 */
public interface ContexteInterpretation {

    void a(MotA motA);

    void b(MotB motB);

    void c(MotC motC);

    void d(MotD motD);

    void e(MotE motE);

    void f(MotF motF);

    void g(MotG motG);
}
